package Ficha_IP;

public class Ficha2_8 {

	public static void main(String[] args) {
		double graus = 120, pi = Math.PI, grados = 90;
		double conversaoGraus = graus * pi / 180;
		double conversaoGrados = graus * 100 / grados;
		System.out.printf("%.3f radianos, %.3f grados",conversaoGraus, conversaoGrados);
	}

}
